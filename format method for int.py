"""-------------------------------------------Integer Datatype-------------------------------------------------------------------"""
print("{0:d}".format(100))
print("{1:d}".format(100,200))
print("{0:5d}".format(500))
print("{1:05d}".format(600,700))
print("{0:*^+10d}".format(400))
print("{0:>05d}".format(300))
print("{:,d}".format(122132324234553454354354300))