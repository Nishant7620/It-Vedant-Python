#Variable length argument

def show(arg,arg1):
    print(arg,arg1)
show(100,200)    

#---------------------------------------------------------------------------------------------------------------------------------
def show(*args):
    print(args[0],args[1])
show(300,400)    

#-------------------------------------------------------------------------------------------------------------------------------

def show (*args):
    print(args[1],args[0])
show(100,200,300,400)

#-------------------------------------------------------------------------------------------------------------------------------

def sum_all(*args):
    result=0
    for num in args:
        result=result+num
        return result
print(sum_all(1,2,3,4,5))        


#---------------------------------------------------------------------------------------------------------------------------------


def show (args,args1):
    print(args,args1)
show (100,200)    

#-------------------------------------------------------------------------------------------------------------------------------

def show (**args):
    print(args["a"],args["b"])
show(a=100,b=200)    


#-----------------------------------------------------------------------------------------------------------------------------

def display_info(**kwargs):
    for key,value in kwargs.items():
        print(f"{key}:{value}")
display_info(name="Nishant", age=23,city="Panvel")        