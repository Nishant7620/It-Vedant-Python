#getting element by using index value

my_list = [1,2,3,4,5,6,7,8]

print(my_list[2:5])

#--------------------------------------------------------------------------------------------------------------------------------------

my_list = [1,2,3,4,5,6,7,8]

print(my_list[2:5:2])

#------------------------------------------------------------------------------------------------------------------------------------
#get all element from index value

my_list = [1,2,3,4,5,6,7,8]

print(my_list[3:])

#----------------------------------------------------------------------------------------------------------------------------------
#get all element from beginning to index

my_list = [0,1,2,3,4,5,6,7,8]

print(my_list[:3])

#-------------------------------------------------------------------------------------------------------------------------------
#get a copy of entire list

my_list = [1,2,3,4,5,6,7,8]

result = my_list[:]

print(result)
